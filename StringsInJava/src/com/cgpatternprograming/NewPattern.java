package com.cgpatternprograming;

public class NewPattern {

	public static void main(String[] args) {
 for(int i=1;i<=3;i++)
 {
	 for(int j=8;j>6;j--)
	 {
		 System.out.print("/");
		 System.out.println("");
	 for(int k=1;k<6;k++)
	 {
		 System.out.print("*");
	 }
	 }
 } System.out.println("");
	}

}
