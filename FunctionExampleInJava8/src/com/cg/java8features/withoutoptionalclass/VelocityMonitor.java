package com.cg.java8features.withoutoptionalclass;

public class VelocityMonitor {

	private int speed;

	public VelocityMonitor() {
		super();
	}

	public VelocityMonitor(int speed) {
		super();
		this.speed = speed;
	}

	public int getSpeed() {
		return speed;
	}

	public void setSpeed(int speed) {
		this.speed = speed;
	}
	
	
}
